CREATE TABLE IF NOT EXISTS "documentation" (
	"id"	INTEGER NOT NULL,
	"identifier"	TEXT NOT NULL,
	"documentation"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "proposals" (
	"id"	INTEGER NOT NULL,
	"user"	TEXT NOT NULL,
	"proposal"	TEXT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
COMMIT;
